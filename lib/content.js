export function getSortedBlogPosts() {
  return [...BLOG_POSTS].sort((left, right) => {
    const leftTime = new Date(left?.publishedAt || 0).getTime();
    const rightTime = new Date(right?.publishedAt || 0).getTime();

    return rightTime - leftTime;
  });
}

export function getBlogPostBySlug(slug) {
  return BLOG_POSTS.find((entry) => entry.slug === slug) || null;
}

export function getBlogPostsByCategory(categoryTitle, { excludeSlug = null, limit = 3 } = {}) {
  const normalizedCategory = normalizeValue(categoryTitle);

  return getSortedBlogPosts()
    .filter((post) => {
      if (!post || post.slug === excludeSlug) {
        return false;
      }

      return (
        normalizeValue(post.category) === normalizedCategory ||
        normalizeValue(post?.productSource?.categoryTitle) === normalizedCategory
      );
    })
    .slice(0, Math.max(0, limit));
}

export function getBlogPostsForProduct(product, { limit = 3 } = {}) {
  const category = product?.category || product?.product_type || "";
  const categoryMatches = getBlogPostsByCategory(category, { limit: limit + 2 });
  const productTitle = normalizeValue(product?.title || product?.name);

  const rankedPosts = getSortedBlogPosts()
    .filter((post) => {
      if (!post) {
        return false;
      }

      const postSearchBlob = [
        post.title,
        post.excerpt,
        post.category,
        ...(post.keywords || []),
        post?.productSource?.title,
      ]
        .filter(Boolean)
        .join(" ")
        .toLowerCase();

      return (
        categoryMatches.some((entry) => entry.slug === post.slug) ||
        (productTitle && postSearchBlob.includes(productTitle))
      );
    })
    .filter((post, index, list) => list.findIndex((entry) => entry.slug === post.slug) === index);

  return rankedPosts.slice(0, Math.max(0, limit));
}

