function normalizeValue(value) {
  return String(value || "").trim().toLowerCase();
}

function slugifyValue(value) {
  return normalizeValue(value)
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

